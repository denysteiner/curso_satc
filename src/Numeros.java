public class Numeros{
	public static void main (String[] args){
		long numeroLong = 10L;
		System.out.println(numeroLong);
		
		numeroLong = 10l;
		System.out.println(numeroLong);
		
		numeroLong = 10;
		System.out.println(numeroLong);
		
		float numeroFloat = 10F;
		System.out.println(numeroFloat);
		
		numeroFloat = 10f;
		System.out.println(numeroFloat);
		
		numeroFloat = 10;
		System.out.println(numeroFloat);
		
		numeroFloat = 10.0F;
		System.out.println(numeroFloat);
		
		numeroFloat = (float) -10E38;
		//System.out.println(numeroFloat);
		
		//numeroFloat = 10E38;
		//System.out.println(numeroFloat);
		
		double numeroDouble = 1E10;
		System.out.println(numeroDouble);
		
		numeroDouble = 1e10;
		System.out.println(numeroDouble);

		numeroDouble = 10D;
		System.out.println(numeroDouble);
		
		numeroDouble = 10d;
		System.out.println(numeroDouble);
		
		numeroDouble = 10.0;
		System.out.println(numeroDouble);
		
	}
}
